package woo;

public class LoadDTO {
	
	String id;
	String pw;
	String img_filename;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the pw
	 */
	public String getPw() {
		return pw;
	}
	/**
	 * @param pw the pw to set
	 */
	public void setPw(String pw) {
		this.pw = pw;
	}
	/**
	 * @return the img_filename
	 */
	public String getImg_filename() {
		return img_filename;
	}
	/**
	 * @param img_filename the img_filename to set
	 */
	public void setImg_filename(String img_filename) {
		this.img_filename = img_filename;
	}
	
}
